#!/usr/bin/env python3
"""
Generate a minimal OpenFAST / ElastoDyn structural-only case around an existing
ElastoDyn tower file generated from CSF.

Outputs:
  - Main.fst
  - ElastoDyn.dat
  - ElastoDyn_Blade.dat

The tower file itself is not regenerated here. It is referenced directly by the
ElastoDyn primary input file.
"""

from __future__ import annotations

import argparse
from pathlib import Path
from typing import Tuple

import yaml


def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(description="Generate minimal OpenFAST case templates around an existing ElastoDyn tower file.")
    p.add_argument("tower_file", help="Existing ElastoDyn tower file, e.g. histwin_tower_ElastoDyn_Tower.dat")
    p.add_argument("--yaml", dest="yaml_file", default=None, help="CSF YAML file to infer tower height (default: try <stem>.yaml)")
    p.add_argument("--outdir", default=".", help="Output directory for generated files")
    p.add_argument("--fst-name", default="Main.fst", help="Main OpenFAST input filename")
    p.add_argument("--ed-name", default="ElastoDyn.dat", help="ElastoDyn primary input filename")
    p.add_argument("--blade-name", default="ElastoDyn_Blade.dat", help="ElastoDyn blade input filename")
    return p.parse_args()


def infer_yaml_path(tower_file: Path, yaml_arg: str | None) -> Path | None:
    if yaml_arg:
        yp = Path(yaml_arg)
        return yp if yp.exists() else None
    stem = tower_file.name
    suffix = "_ElastoDyn_Tower.dat"
    if stem.endswith(suffix):
        candidate = tower_file.with_name(stem[:-len(suffix)] + ".yaml")
        if candidate.exists():
            return candidate
    return None


def read_tower_height(yaml_file: Path) -> Tuple[float, float, float]:
    with open(yaml_file, "r", encoding="utf-8") as f:
        data = yaml.safe_load(f)
    z0 = float(data["CSF"]["sections"]["S0"]["z"])
    z1 = float(data["CSF"]["sections"]["S1"]["z"])
    return z0, z1, z1 - z0


