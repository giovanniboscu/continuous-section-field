def main() -> None:
    args = parse_args()
    tower_file = Path(args.tower_file).resolve()
    if not tower_file.exists():
        raise SystemExit(f"Tower file not found: {tower_file}")

    yaml_path = infer_yaml_path(tower_file, args.yaml_file)
    if yaml_path is not None:
        z0, z1, tower_height = read_tower_height(yaml_path)
    else:
        z0, z1, tower_height = 0.0, 0.0, 0.0

    outdir = Path(args.outdir).resolve()
    outdir.mkdir(parents=True, exist_ok=True)

    fst_path = outdir / args.fst_name
    ed_path = outdir / args.ed_name
    blade_path = outdir / args.blade_name

    write_main_fst(fst_path, args.ed_name)
    write_elastodyn(ed_path, args.blade_name, tower_file.name, tower_height)
    write_blade(blade_path)

    print(f"Generated: {fst_path}")
    print(f"Generated: {ed_path}")
    print(f"Generated: {blade_path}")
    if yaml_path is not None:
        print(f"Tower height inferred from YAML: z0={z0}, z1={z1}, H={tower_height}")
    else:
        print("WARNING: YAML not found. TowerHt left at 0.0 in ElastoDyn.dat; edit manually.")
    print("NOTE: non-CSF machine data remain the user's responsibility.")


if __name__ == "__main__":
    main()
