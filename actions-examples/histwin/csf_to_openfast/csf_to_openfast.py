def run_openfast(exe: str, fst_file: str) -> None:
    step(7, f"Running OpenFAST: {exe}  {fst_file}")
    subprocess.run(
        [str(Path(exe).resolve()), str(Path(fst_file).resolve())],
        check=True,
    )


# =============================================================================
# argparse
# =============================================================================

def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(
        description="Generate OpenFAST minimal input files from a CSF geometry YAML (no SubDyn).",
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )
    p.add_argument("yaml", help="CSF geometry YAML file")

    mat = p.add_argument_group("material")
    mat.add_argument("--E",   type=float, required=True, help="Young's modulus [Pa]")
    mat.add_argument("--rho", type=float, required=True, help="Density [kg/m3]")

    disc = p.add_argument_group("tower discretization")
    disc.add_argument("--n-stations", type=int, required=True, help="Number of tower stations for ElastoDyn_Tower.dat")

    rna = p.add_argument_group("RNA (Rotor-Nacelle Assembly)")
    rna.add_argument("--rna-mass",  type=float, required=True, help="Total RNA mass [kg]")
    rna.add_argument("--rna-ixx",   type=float, required=True, help="Side-to-side inertia [kg m^2] (stored only for logging)")
    rna.add_argument("--rna-iyy",   type=float, required=True, help="Fore-aft inertia [kg m^2] (stored only for logging)")
    rna.add_argument("--rna-izz",   type=float, required=True, help="Yaw inertia [kg m^2]")
    rna.add_argument("--rna-cm-x",  type=float, required=True, help="CM downstream offset from tower-top [m]")
    rna.add_argument("--rna-cm-z",  type=float, required=True, help="CM vertical offset from tower-top [m]")
    rna.add_argument("--hub-rad",   type=float, required=True, help="Hub radius [m]")
    rna.add_argument("--overhang",  type=float, required=True, help="Rotor apex offset from yaw axis [m]")

    sim = p.add_argument_group("simulation")
    sim.add_argument("--tmax", type=float, required=True, help="Simulation duration [s]")
    sim.add_argument("--dt",   type=float, required=True, help="Time step [s]")

    p.add_argument("--out",          type=str, default=None, help="Output filename stem (default: YAML stem)")
    p.add_argument("--openfast-exe", type=str, default=None, help="Path to OpenFAST executable (optional)")

    return p.parse_args()


# =============================================================================
# Main
# =============================================================================

def main() -> None:
    print()
    print(f"[csf_to_openfast v{VERSION}]  CSF -> OpenFAST minimal pipeline")
    print("=" * 65)

    args = parse_args()

    yaml_path = args.yaml
    out_dir   = os.getcwd()
    stem      = args.out if args.out else os.path.splitext(os.path.basename(yaml_path))[0]
    base      = stem

    twr_file = os.path.join(out_dir, base + "_ElastoDyn_Tower.dat")
    ed_file  = os.path.join(out_dir, base + "_ElastoDyn.dat")
    bld_file = os.path.join(out_dir, base + "_ElastoDyn_Blade.dat")
    fst_file = os.path.join(out_dir, base + "_Main.fst")

    rna = {
        "mass":     args.rna_mass,
        "Ixx":      args.rna_ixx,
        "Iyy":      args.rna_iyy,
        "Izz":      args.rna_izz,
        "cm_x":     args.rna_cm_x,
        "cm_z":     args.rna_cm_z,
        "hub_rad":  args.hub_rad,
        "overhang": args.overhang,
    }

    try:
        field, z0, z1 = load_csf(yaml_path)
        props = compute_properties(field, z0, z1, args.n_stations)
        write_elastodyn_tower(twr_file, props, z0, z1, args.E, args.rho, yaml_path)
        write_elastodyn_blade(bld_file, args.hub_rad, yaml_path)
        write_elastodyn(ed_file, twr_file, bld_file, z0, z1, rna, yaml_path)
        write_main_fst(fst_file, ed_file, args.tmax, args.dt, yaml_path)
    except Exception as exc:
        print()
        print(f"[csf_to_openfast v{VERSION}]  ERROR: {exc}")
        raise

    print()
    print("=" * 65)
    print(f"[csf_to_openfast v{VERSION}]  Generated files:")
    for f in (twr_file, ed_file, bld_file, fst_file):
        print(f"  {f}")
    print()

    if args.openfast_exe:
        run_openfast(args.openfast_exe, fst_file)


if __name__ == "__main__":
    main()
