# CSF helpers
# =============================================================================

def load_csf(yaml_path: str):
    step(1, f"Loading CSF geometry from: {yaml_path}")
    read_result = CSFReader().read_file(yaml_path)
    field = read_result.field

    with open(yaml_path, encoding="utf-8") as f:
        data = yaml.safe_load(f)

    z0 = float(data["CSF"]["sections"]["S0"]["z"])
    z1 = float(data["CSF"]["sections"]["S1"]["z"])

    if read_result.issues:
        for issue in read_result.issues:
            print(f"  CSF issue: {issue.severity.value} {issue.code} - {issue.message}")

    print(f"  z range : {z0:.3f} -> {z1:.3f} m   H = {z1 - z0:.3f} m")
    return field, z0, z1


def compute_properties(field, z0: float, z1: float, n: int) -> list[dict]:
    step(2, f"Computing section properties at {n} stations via section_full_analysis")
    z_stations = np.linspace(z0, z1, n)
    results = []

    for z in z_stations:
        sa = section_full_analysis(field.section(float(z)))

        results.append({
            "z":  float(z),
            "A":  float(sa["A"]),
            "Ix": float(sa["Ix"]),
            "Iy": float(sa["Iy"]),
        })

    print(f"  A_base = {results[0]['A']:.6f} m2   A_top = {results[-1]['A']:.6f} m2")
    return results


