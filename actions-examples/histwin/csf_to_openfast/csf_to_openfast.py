def write_elastodyn_tower(
    outfile: str,
    props: list[dict],
    z0: float,
    z1: float,
    E: float,
    rho: float,
    yaml_path: str,
) -> None:
    """Write ElastoDyn tower properties file.

    The file is parser-compatible with OpenFAST v5.x.
    Distributed properties are derived directly from CSF:
        TMassDen = rho * A
        TwFAStif = E * Ix
        TwSSStif = E * Iy

    Tower mode-shape coefficients are simple parser-valid placeholders.
    """
    step(3, f"Writing ElastoDyn tower: {outfile}")

    H = z1 - z0

    with open(outfile, "w", encoding="utf-8") as f:
        f.write("------- ELASTODYN V1.00.* TOWER INPUT FILE -------------------------------------\n")
        f.write(f"Generated by csf_to_openfast.py v{VERSION} from: {yaml_path}\n")

        f.write("---------------------- TOWER PARAMETERS ----------------------------------------\n")
        f.write(f"{len(props):11d}   NTwInpSt    - Number of input stations to specify tower geometry\n")
        f.write("        1.0   TwrFADmp(1) - Tower 1st fore-aft mode structural damping ratio (%)\n")
        f.write("        1.0   TwrFADmp(2) - Tower 2nd fore-aft mode structural damping ratio (%)\n")
        f.write("        1.0   TwrSSDmp(1) - Tower 1st side-to-side mode structural damping ratio (%)\n")
        f.write("        1.0   TwrSSDmp(2) - Tower 2nd side-to-side mode structural damping ratio (%)\n")

        f.write("---------------------- TOWER ADJUSTMENT FACTORS -------------------------------\n")
        f.write("          1   FAStTunr(1) - Tower fore-aft modal stiffness tuner, 1st mode (-)\n")
        f.write("          1   FAStTunr(2) - Tower fore-aft modal stiffness tuner, 2nd mode (-)\n")
        f.write("          1   SSStTunr(1) - Tower side-to-side stiffness tuner, 1st mode (-)\n")
        f.write("          1   SSStTunr(2) - Tower side-to-side stiffness tuner, 2nd mode (-)\n")
        f.write("          1   AdjTwMa     - Factor to adjust tower mass density (-)\n")
        f.write("          1   AdjFASt     - Factor to adjust tower fore-aft stiffness (-)\n")
        f.write("          1   AdjSSSt     - Factor to adjust tower side-to-side stiffness (-)\n")

        f.write("---------------------- DISTRIBUTED TOWER PROPERTIES ---------------------------\n")
        f.write("        HtFract                 TMassDen                 TwFAStif                 TwSSStif\n")
        f.write("          (-)                   (kg/m)                   (Nm^2)                   (Nm^2)\n")
        for p in props:
            hfrac = (p["z"] - z0) / H if H > 0.0 else 0.0
            mass_den = rho * p["A"]
            tw_fa = E * p["Ix"]
            tw_ss = E * p["Iy"]
            f.write(
                f"{hfrac:22.15E}  {mass_den:22.15E}  {tw_fa:22.15E}  {tw_ss:22.15E}\n"
            )

        f.write("---------------------- TOWER FORE-AFT MODE SHAPES -----------------------------\n")
        f.write("        1.0   TwFAM1Sh(2) - Mode 1, coefficient of x^2 term\n")
        f.write("        0.0   TwFAM1Sh(3) -         , coefficient of x^3 term\n")
        f.write("        0.0   TwFAM1Sh(4) -         , coefficient of x^4 term\n")
        f.write("        0.0   TwFAM1Sh(5) -         , coefficient of x^5 term\n")
        f.write("        0.0   TwFAM1Sh(6) -         , coefficient of x^6 term\n")
        f.write("        0.0   TwFAM2Sh(2) - Mode 2, coefficient of x^2 term\n")
        f.write("        0.0   TwFAM2Sh(3) -         , coefficient of x^3 term\n")
        f.write("        0.0   TwFAM2Sh(4) -         , coefficient of x^4 term\n")
        f.write("        0.0   TwFAM2Sh(5) -         , coefficient of x^5 term\n")
        f.write("        1.0   TwFAM2Sh(6) -         , coefficient of x^6 term\n")

        f.write("---------------------- TOWER SIDE-TO-SIDE MODE SHAPES ------------------------\n")
        f.write("        1.0   TwSSM1Sh(2) - Mode 1, coefficient of x^2 term\n")
        f.write("        0.0   TwSSM1Sh(3) -         , coefficient of x^3 term\n")
        f.write("        0.0   TwSSM1Sh(4) -         , coefficient of x^4 term\n")
        f.write("        0.0   TwSSM1Sh(5) -         , coefficient of x^5 term\n")
        f.write("        0.0   TwSSM1Sh(6) -         , coefficient of x^6 term\n")
        f.write("        0.0   TwSSM2Sh(2) - Mode 2, coefficient of x^2 term\n")
        f.write("        0.0   TwSSM2Sh(3) -         , coefficient of x^3 term\n")
        f.write("        0.0   TwSSM2Sh(4) -         , coefficient of x^4 term\n")
        f.write("        0.0   TwSSM2Sh(5) -         , coefficient of x^5 term\n")
        f.write("        1.0   TwSSM2Sh(6) -         , coefficient of x^6 term\n")

    print(f"  Tower file written with {len(props)} stations")
    print(f"  TMassDen base/top = {rho * props[0]['A']:.3f} / {rho * props[-1]['A']:.3f} kg/m")


