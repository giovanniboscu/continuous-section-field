def build_geometry(args):
    # Both sections use the same parameter family and the same builder.
    s0 = build_section_geometry(
        cx=args.s0_x,
        cy=args.s0_y,
        dx=args.s0_dx,
        dy=args.s0_dy,
        R=args.s0_R,
        tg=args.s0_tg,
        N=args.N,
        n_bars_row1=args.n_bars_row1,
        n_bars_row2=args.n_bars_row2,
        dist_row1_outer=args.dist_row1_outer,
        dist_row2_inner=args.dist_row2_inner,
        twist_deg=0.0,
    )

    s1 = build_section_geometry(
        cx=args.s1_x,
        cy=args.s1_y,
        dx=args.s1_dx,
        dy=args.s1_dy,
        R=args.s1_R,
        tg=args.s1_tg,
        N=args.N,
        n_bars_row1=args.n_bars_row1,
        n_bars_row2=args.n_bars_row2,
        dist_row1_outer=args.dist_row1_outer,
        dist_row2_inner=args.dist_row2_inner,
        twist_deg=args.twist_deg,
    )

    data = {
        "CSF": {
            "sections": {
                "S0": build_section_dict(args.z0, s0, "base", args.s0_t_cell, args),
                "S1": build_section_dict(args.z1, s1, "head", args.s1_t_cell, args),
            }
        }
    }

    weight_laws = build_weight_laws(args)
    if weight_laws:
        data["CSF"]["weight_laws"] = weight_laws
    #print(f"DEBUG {weight_laws}")
    return data


